// Duration: 300 random = 6 Months, 500 random = 8 Months, remaining 923 = 12 Months
// Fees: £6,000 (Total) for all diplomas
// Codes: OSCT-Dip-5118 to OSCT-Dip-6840

const generatePrograms = (categories: [string, string[]][]) => {
  let code = 5118;
  // First pass: build flat list
  const flat: { cat: string; title: string; codeStr: string }[] = [];
  for (const [cat, titles] of categories) {
    for (const title of titles) {
      flat.push({ cat, title, codeStr: `OSCT-Dip-${code.toString().padStart(4, '0')}` });
      code++;
    }
  }

  // Create duration assignments: exactly 300 × 6 Months, 500 × 8 Months, rest × 12 Months
  const durations: string[] = [
    ...Array(300).fill('6 Months'),
    ...Array(500).fill('8 Months'),
    ...Array(flat.length - 800).fill('12 Months'),
  ];
  // Deterministic shuffle so the distribution looks random but stays stable
  for (let i = durations.length - 1; i > 0; i--) {
    const j = ((i * 2654435761) >>> 0) % (i + 1);
    [durations[i], durations[j]] = [durations[j], durations[i]];
  }

  // Build result with duration
  const result: Record<string, { code: string; title: string; duration: string }[]> = {};
  for (let i = 0; i < flat.length; i++) {
    const p = flat[i];
    if (!result[p.cat]) result[p.cat] = [];
    result[p.cat].push({ code: p.codeStr, title: p.title, duration: durations[i] });
  }
  return result;
};

